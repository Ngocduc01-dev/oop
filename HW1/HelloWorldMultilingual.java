public class HelloWorldMultilingual {
    public static void main(String[] args) {
        System.out.println("Hello, World"); //English
        System.out.println("Xin chào thế giới"); //Vietnamese
        System.out.println("你好世界"); //Chinese
        System.out.println("Γεια σου, κόσμε"); //Greek
        System.out.println("こんにちは世界"); //Japanese
        System.out.println("안녕하세요, 세상"); //Korean
        System.out.println("Привет, мир"); //Russia
        System.out.println("Bonjour le monde"); //France
        System.out.println("Hola Mundo"); //Spanish
        System.out.println("Olá, Mundo"); //Portuguese
        System.out.println("مرحبا بالعالم"); //Arab
    }
}
